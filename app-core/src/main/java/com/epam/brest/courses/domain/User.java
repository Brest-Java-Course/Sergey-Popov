package com.epam.brest.courses.domain;

public class User {
	private Long userId;
	private String login;
	private String name;

	public Long getUserId() {
		return userId;	
	}

	public void setUserId(Long userID) {
		this.userId = userId;	
	}

	public getLogin() {
		return login;	
	}

	public setLogin(String login) {
		this.login = login;
	}

	public getName() {
		return name;	
	}

	public setName(String name) {
		this.name = name;	
	}
}
